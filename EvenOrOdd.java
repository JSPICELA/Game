public class EvenOrOdd {
    public static void main(String[] args) {

        for (int i = 0; i<21; i++ ) {
            System.out.println();
            if (i % 2 == 0) {
                System.out.println(i + "- even");
            } else {
                System.out.println(i + "- odd ");
            }
        }
    }
}
